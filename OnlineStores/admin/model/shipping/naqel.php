  public function createWaybill($xml_post_string){
      $url       = 'https://' . self::HOST . $this->_getBaseUrl() . '?op=CreateWaybill';
      $headers = array(
        "POST "  . $this->_getBaseUrl() . ' HTTP/1.1',
        "Host: " . self::HOST,
        "Content-Type: text/xml; charset=utf-8",
        "Content-Length: ".strlen($xml_post_string),
        "SOAPAction: http://tempuri.org/CreateWaybill"
      ); 

      $ch = curl_init();
      curl_setopt($ch, CURLOPT_URL, $url);
      curl_setopt($ch, CURLOPT_POST, true);
      curl_setopt($ch, CURLOPT_POSTFIELDS, $xml_post_string);
      curl_setopt($ch, CURLOPT_HTTPHEADER, $headers);
      curl_setopt($ch, CURLOPT_RETURNTRANSFER, 1);
      $response = curl_exec($ch);
      curl_close($ch);
      $response1 = str_replace("<soap:Body>","",$response);
      $response2 = str_replace("</soap:Body>","",$response1);

      $parser = simplexml_load_string($response2);
      return (array)$parser->CreateWaybillResponse->CreateWaybillResult;
     
  }

