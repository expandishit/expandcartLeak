  /* helper functions */
  private function _getBaseUrl(){
    //Check current API Mode..
    $debugging_mode = $this->config->get('naqel_debugging_mode');
    return ( isset($debugging_mode) && $debugging_mode == 1 ) ? self::API_URL_BASE_TESTING : self::API_URL_BASE_LIVE;
  }

    public function addShipmentDetails($orderId, $details, $status)
    {
        $query = $fields = [];

        $query[] = 'INSERT INTO  ' . DB_PREFIX . 'shipments_details SET';
        $fields[] = 'order_id="' . $orderId . '"';
        $fields[] = 'details=\'' . json_encode($details,JSON_UNESCAPED_UNICODE) . '\'';
        $fields[] = 'shipment_status="' . $status . '"';
        $fields[] = 'shipment_operator="naqel"';
        $query[] = implode(', ', $fields);
        $this->db->query(implode(' ', $query));
    }

    public function getShipmentDetails($orderId)
    {
        $result = $this->db->query("SELECT * FROM  " . DB_PREFIX . "shipments_details where `order_id` = $orderId AND `shipment_operator` = 'naqel' ");

        return $result->row;

    }

